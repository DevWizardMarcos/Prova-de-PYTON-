def calcular_area_retangulo(largura, altura):
    return largura * altura # retora a altura e altura * = vs 

# Atribuir diretamente os valores de largura e altura
largura = 20
altura = 40

# Calcular a área juntando a largura e a a altura
area = calcular_area_retangulo(largura, altura) # faz o calculo da Area


print(f"A área do retângulo é: {area:.2f}") # Exibe o resultado
